#include<iostream>
using namespace std;

int main(){
    int a; //declaration
    a=12; //initialization
    cout<<"size of integer "<< sizeof(a) << endl;

    float b;
    cout<<"size of float "<< sizeof(b) << endl;

    double c;
    cout<<"size of double "<< sizeof(c) << endl;

    short int d;
    cout<<"size of short integer "<< sizeof(d) <<endl;

    long int e;
    cout<<"size of long integer "<< sizeof(e) <<endl;

    char f;
    cout<<"size of character "<< sizeof(f) <<endl;

    bool g;
    cout<<"size of boolean "<< sizeof(g) <<endl;

    return 0;

}